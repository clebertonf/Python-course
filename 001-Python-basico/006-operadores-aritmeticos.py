# Operadores aritméticos

"""
    + soma
    - subtração
    * multiplicação
    / divisão
    // divisão inteira
    ** exponenciaçãopresce
    % resto da divisão
    () precedência
"""

print(10 + 10)
print(10 - 5)
print(10 * 3)
print(10 / 2)
print(10 // 2)
print(10 ** 2)
print(10 % 2)

print((10 + 2) * 3)  # Parênteses tem prioridade

print(10 * 'Cleber - ')  # Imprime a string (Cleber) 10 vezes
